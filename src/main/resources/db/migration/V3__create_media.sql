CREATE TABLE MEDIAS (
                        ID NUMBER(19, 0) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        TITLE VARCHAR2(255) NOT NULL,
                        TYPE VARCHAR2(50) NOT NULL,
                        DURATION NUMBER(10, 2) DEFAULT 0.0 NOT NULL,
                        EPISODES NUMBER(10, 0) DEFAULT 0 NOT NULL,
                        SEASONS NUMBER(10, 0) DEFAULT 0 NOT NULL
);

CREATE TABLE MEDIAS_GENRES (
                               MEDIA_ID NUMBER(19, 0) NOT NULL,
                               GENRE_ID NUMBER(19, 0) NOT NULL,

                               CONSTRAINT PK_MEDIAS_GENRES PRIMARY KEY (MEDIA_ID, GENRE_ID),
                               CONSTRAINT FK_MG_MEDIA FOREIGN KEY (MEDIA_ID) REFERENCES MEDIAS(ID) ON DELETE CASCADE,
                               CONSTRAINT FK_MG_GENRE FOREIGN KEY (GENRE_ID) REFERENCES CINESUGGEST_USER.GENRES(ID) ON DELETE CASCADE
);